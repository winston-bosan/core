package com.liveasy.demo.model;

import javax.persistence.*;
import java.util.HashSet;
import java.util.Set;

@Entity
public class User {

    /*
    The Id of the User. Generated at Sign-up;
    It is Generated by Strategy Identity. Non-Nullable, and acts as one of the hash variables.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /*
    The Email of the User. Required at Sign-up.
     */
    //@NotEmpty(message = "* Please provide an Email!")
    //@Email(message = "* Please provide a valid Email!")
    private String email;

    /*
    The Password of the User. Required at Sign-up.
     */
    private String password;
    private String firstName;
    private String lastName;
    private int active;

    @OneToMany(cascade = CascadeType.ALL, mappedBy = "user")
    @OrderBy("id")
    private Set<House> houses = new HashSet<>();
    @Enumerated(value = EnumType.STRING)
    private Role role;











    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getActive() {
        return active;
    }

    public void setActive(int active) {
        this.active = active;
    }

    public Set<House> getHouses() {
        return houses;
    }

    public void setHouses(Set<House> houses) {
        this.houses = houses;
    }

    public void addHouse(House house){
        house.setUser(this);
        houses.add(house);
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }
}
